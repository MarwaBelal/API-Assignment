package purchaseahotel;

public class location {
	private String address;
	private String area;
	private String city;
	private String country;
	private String recommended_transport;
	private String state;
	private String zip_codes;
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getRecommended_transport() {
		return recommended_transport;
	}
	public void setRecommended_transport(String recommended_transport) {
		this.recommended_transport = recommended_transport;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZip_codes() {
		return zip_codes;
	}
	public void setZip_codes(String zip_codes) {
		this.zip_codes = zip_codes;
	}
	
	
	

}
